//Arithmetic operators are used for mathematical calculations.
public class ArithmeticOperator {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        System.out.println("Addition(+): " +(a+b));//Add the number
        System.out.println("Subtraction(-): " +(a-b));//Subtract the number
        System.out.println("Multiplication(*): " +(a*b));//multiply the number
        System.out.println("Division(/): " +(a/b));//Divide the number
        System.out.println("Modulus(%): "+(a%b));////Return the remender
    }
}
