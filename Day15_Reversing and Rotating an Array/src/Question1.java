// Using temp variable.
public class Question1 {
    public static void main(String[] args) {
     int a = 5;
     int b = 3;
     int temp ;
     temp = a;
     a = b ;
     b = temp;
        System.out.println("A is: "+a);
        System.out.println("B is: "+b);
    }
}
