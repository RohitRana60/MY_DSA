//Swap to number using a variable
public class Question05 {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;
        int temp;
        System.out.println("The valur of a is: " +a);
        System.out.println("The valur of b is: " +b);
        temp = a;
        a = b;
        b = temp;
        System.out.println("The valur of a is: " +a);
        System.out.println("The valur of b is: " +b);
    }
}
